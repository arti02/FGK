package fgk.zad1.Utilitis;

public class Reductor {
    public static double min(double p1, double p2)
    {
        if(p1<p2)
        {
            return p1;
        }
        else
        {
            return p2;
        }
    }
    public static double max(double p1, double p2)
    {
        if(p1>p2)
        {
            return p1;
        }
        else
        {
            return p2;
        }
    }
}
